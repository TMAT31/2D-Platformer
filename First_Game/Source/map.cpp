Map::Map() {
	grass = TextureManager::LoadTexture("Assets/grass.png");

	LoadMap(lvl1);

	src.h = 124;
	src.w = 124;
	src.x = src.y = 0;
	dest.h = 124;
	dest.w = 124;
	dest.x = dest.y = 0;
}

void Map::LoadMap(int arr[20][25]) {
	for (int row = 0; row < 20; row++) {
		for (int col = 0; col < 25; col++) {
			lvl1[row][col] = arr[row][col];
		}
	}
}

void Map::DrawMap() {
	int type = 0;
	for (int row = 0; row < 20; row++) {
		for (int col = 0; col < 25; col++) {
			type = lvl1[row][col];
			dest.x = col * 124;
			dest.y = row * 124;
			switch (type) {
			case 0:
				TextureManager::Draw(grass, src, dest);
				break;
			default:
				break;
			}
		}
	}
}